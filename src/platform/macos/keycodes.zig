const platform = @import("../platform.zig");

/// Maps macOS virtual key codes to platform-independent key enum
pub fn keyFromKeyCode(key_code: u16) platform.Key {
    return switch (key_code) {
        0x00 => .a,
        0x01 => .s,
        0x02 => .d,
        0x03 => .f,
        0x04 => .h,
        0x05 => .g,
        0x06 => .z,
        0x07 => .x,
        0x08 => .c,
        0x09 => .v,
        0x0B => .b,
        0x0C => .q,
        0x0D => .w,
        0x0E => .e,
        0x0F => .r,
        0x10 => .y,
        0x11 => .t,
        0x12 => .@"1",
        0x13 => .@"2",
        0x14 => .@"3",
        0x15 => .@"4",
        0x16 => .@"6",
        0x17 => .@"5",
        0x18 => .equal,
        0x19 => .@"9",
        0x1A => .@"7",
        0x1B => .minus,
        0x1C => .@"8",
        0x1D => .@"0",
        0x1E => .rightbrace,
        0x1F => .o,
        0x20 => .u,
        0x21 => .leftbrace,
        0x22 => .i,
        0x23 => .p,
        0x24 => .enter,
        0x25 => .l,
        0x26 => .j,
        0x27 => .apostrophe,
        0x28 => .k,
        0x29 => .semicolon,
        0x2A => .backslash,
        0x2B => .comma,
        0x2C => .slash,
        0x2D => .n,
        0x2E => .m,
        0x2F => .dot,
        0x30 => .tab,
        0x31 => .space,
        0x32 => .grave,
        0x33 => .backspace,
        0x35 => .esc,
        0x36 => .rightmeta,
        0x37 => .leftmeta,
        0x38 => .leftshift,
        0x39 => .capslock,
        0x3A => .leftalt,
        0x3B => .leftctrl,
        0x3C => .rightshift,
        0x3D => .rightalt,
        0x3E => .rightctrl,
        0x60 => .f5,
        0x61 => .f6,
        0x62 => .f7,
        0x63 => .f3,
        0x64 => .f8,
        0x65 => .f9,
        0x67 => .f11,
        0x6D => .f10,
        0x6F => .f12,
        0x72 => .insert,
        0x73 => .home,
        0x74 => .page_up,
        0x75 => .delete,
        0x76 => .f4,
        0x77 => .end,
        0x78 => .f2,
        0x79 => .page_down,
        0x7A => .f1,
        0x7B => .left,
        0x7C => .right,
        0x7D => .down,
        0x7E => .up,
        else => .null,
    };
}
